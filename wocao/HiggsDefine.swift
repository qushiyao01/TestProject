import Foundation
import UIKit
let RGBMainColor = UIColor(red: 84/255.0, green: 93/255.0, blue: 255/255.0, alpha: 1)
func RGB(r:CGFloat,_ g:CGFloat,_ b:CGFloat, alpha:CGFloat) ->UIColor
{
return UIColor(red: (r)/255.0, green: (g)/255.0, blue: (b)/255.0, alpha: alpha)
}

